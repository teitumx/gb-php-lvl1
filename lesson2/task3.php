<?php

//Сложение 
function sum($a, $b) 
{
    return $a + $b;
}
echo sum(5, 3) .'<br>';

//Вычитание
function sub($a, $b)
{
    return $a - $b;
}
echo sub(6, 2) .'<br>';

//Умножение
function multi($a, $b) 
{
    return $a * $b;
}
echo multi(7, 3) .'<br>';

//Деление
function div($a, $b) 
{
    return $a / $b;
}
echo div(9, 3);

?>